package com.kuier.tool.core.Text;

/**
 * @ClassName CharPool
 * @Description CharPool 常用字符常量接口
 * @Author LiuQi
 * @Date 2024/8/2 17:44
 * @Version 1.0
 */
public interface CharPool {
    /**
     * 字符常量：斜杠
     */
    char SLASH = '/';

    /**
     * 字符常量：反斜杠
     */
    char BACKSLASH = '\\';

    /**
     * 字符常量：空格符
     */
    char SPACE = ' ';
}
