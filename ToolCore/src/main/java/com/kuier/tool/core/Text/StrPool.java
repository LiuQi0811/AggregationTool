package com.kuier.tool.core.Text;

/**
 * @ClassName StrPool
 * @Description StrPool 常用字符串常量接口
 * @Author LiuQi
 * @Date 2024/8/2 16:50
 * @Version 1.0
 */
public interface StrPool {
    /**
     * 字符常量：斜杠
     */
    char C_SLASH = CharPool.SLASH;

    /**
     * 字符串常量：斜杠
     */
    String SLASH = "/";

    /**
     * 字符常量：反斜杠
     */
    char C_BACKSLASH = CharPool.BACKSLASH;

    /**
     * 字符串常量：空 JSON
     */
    String EMPTY_JSON = "{}";

    /**
     * 字符串常量：冒号
     */
    String COLON = ":";
}
